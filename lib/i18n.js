export function makeT(lang) {
  const dict = translations[lang] || translations.sv;
  const fallback = translations.en;
  return (key, vars) => {
    let s = dict[key];
    if (s == null) s = fallback[key];
    if (s == null) s = key;
    if (vars) for (const k in vars) s = s.split("{" + k + "}").join(String(vars[k]));
    return s;
  };
}
